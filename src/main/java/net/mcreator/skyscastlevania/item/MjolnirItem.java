@SkysCastlevaniaModElements.ModElement.Tag
public class MjolnirItem extends SkysCastlevaniaModElements.ModElement {
	@ObjectHolder("skys_castlevania:mjolnir")
	public static final Item block = null;

	public MjolnirItem(SkysCastlevaniaModElements instance) {
		super(instance, 28);
	}

	@Override
	public void initElements() {
		elements.items.add(() -> new AxeItem(new IItemTier() {
			public int getMaxUses() {
				return 1500;
			}

			public float getEfficiency() {
				return 4f;
			}

			public float getAttackDamage() {
				return 8f;
			}

			public int getHarvestLevel() {
				return 2;
			}

			public int getEnchantability() {
				return 2;
			}

			public Ingredient getRepairMaterial() {
				return Ingredient.fromStacks(new ItemStack(ElectrumIngotItem.block));
			}
		}, 1, -2.6f, new Item.Properties().group(SkysCastlevaniaTabItemGroup.tab)) {
			@Override
			public void addInformation(ItemStack itemstack, World world, List<ITextComponent> list, ITooltipFlag flag) {
				super.addInformation(itemstack, world, list, flag);
				list.add(new StringTextComponent("The hammer of the thunder-god Thor."));
			}
		}.setRegistryName("mjolnir"));
	}
}
